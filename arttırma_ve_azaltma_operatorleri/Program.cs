using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace arttırma_ve_azaltma_operatorleri
{
    internal class Program
    {
        static void Main(string[] args)
        {
            int x = 20;
            int y = 40;
            x++; // x'i 1 arttırır.
            y--; // y'yi 1 azaltır.
            Console.WriteLine("x in yeni değeri: " + x);
            Console.WriteLine("y nin yeni değeri: " + y);
            Console.ReadLine();
        }
    }
}
